package com.udemy.carros.entities;

public class Car {

    public int id;
    public String model;
    public int horsePower;
    public double price;

    public Car(int id, String model, int horsePower, double price) {
        this.id = id;
        this.model = model;
        this.horsePower = horsePower;
        this.price = price;
    }




}
